public class ArithmeticOperators {
    public static void main(String[] args) {
     //Arithmetic operators have two types
        //Binary operator : +, -, *, /, % works with 2 operands
        //Unary operator : ++, --  works with 1 operand


        int a=7;
        System.out.println(a++); //a will be printed 7 and then post increment now value of a is 8
        System.out.println(++a); //now value of a is 8 and pre increment will increasae value by 9 and it will be printed





    }
}
