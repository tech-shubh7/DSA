public class LogicalOperators {
    public static void main(String[] args) {
//        LogicalOperators : && (logical AND) , || (logical OR), ! (logical NOT)

        boolean a;
        System.out.println( (7>9) && (a) );
        System.out.println( (7>9) || (9>7) );
        System.out.println(!(9>7));


    }
}
